package assesment;


public class You {
		private int xcoord;
		private int ycoord;
		
		public You(int xcoord, int ycoord) {
			super();
			this.xcoord = xcoord;
			this.ycoord = ycoord;
		}

		public int getXcoord() {
			return xcoord;
		}

		public void setXcoord(int xcoord) {
			this.xcoord = xcoord;
		}

		public int getYcoord() {
			return ycoord;
		}

		public void setYcoord(int ycoord) {
			this.ycoord = ycoord;
		}
		
	
}
